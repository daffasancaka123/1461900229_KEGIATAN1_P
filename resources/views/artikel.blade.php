<p>Definisi Framework</p>
<p style="text-align:center;">Sesuai penamaan istilahnya, kata “framework” merujuk pada kerangka kerja yang memberikan struktur dasar dalam pembangunan sebuah web. Penggunaan kerangka kerja ini membantu kamu mengembangkan suatu web lebih cepat. Coba ingat lagi saat kamu belajar dasar-dasar PHP. Kamu harus menuliskan serangkaian coding untuk memberi tampilan tertentu pada web kan? Berkat framework, kamu tidak usah menuliskan kode yang sama berulang kali. Plus, aplikasi atau web tersebut akan lebih stabil karena framework membantu interaksi database dan coding benar-benar sudah berjalan optimal.</p>
